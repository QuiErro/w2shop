 $(function(){
 	//显示注册页面
	$(".go_reg").click(function(){
		$("#reg").show();
		$("#log").hide();
	});
	//显示登录页面
	$(".go_log").click(function(){
		$("#log").show();
		$("#reg").hide();
	});
	//关于文本框显示的特效
	$("#log_name").focusin(function(){
		$(".label_name").addClass("on_name");
	});
	$("#log_pass").focusin(function(){
		$(".label_pass").addClass("on_pass");
	});
	$("#log_name").focusout(function(){
		$(".label_name").removeClass("on_name");
	});
	$("#log_pass").focusout(function(){
		$(".label_pass").removeClass("on_pass");
	});
	$("#reg_name").focusin(function(){
		$(".label_name").addClass("on_name");
	});
	$("#reg_pass").focusin(function(){
		$(".label_pass").addClass("on_pass");
	});
	$("#reg_name").focusout(function(){
		$(".label_name").removeClass("on_name");
	});
	$("#reg_pass").focusout(function(){
		$(".label_pass").removeClass("on_pass");
	});
	$("#reg_email").focusin(function(){
		$(".label_email").addClass("on_email");
	});
	$("#reg_email").focusout(function(){
		$(".label_email").removeClass("on_email");
	});
	$("#log_code").focusin(function(){
		$(".label_code").addClass("on_code");
	});
	$("#log_code").focusout(function(){
		$(".label_code").removeClass("on_code");
	});
	$("#reg_pass_check").focusin(function(){
		$(".label_pass_check").addClass("on_pass_check");
	});
	$("#reg_pass_check").focusout(function(){
		$(".label_pass_check").removeClass("on_pass_check");
	});
	//正则检验邮箱是否符合要求
	$("#reg").submit(function(ev){
		var re = /^\w+@[a-z0-9]+\.\w+$/i;
		var oText_email = $("#reg_email").val();
		if(!re.test(oText_email)){
			$(".email_tips").show();
			ev.preventDefault();
		}
		if($("#reg_pass").val().length < 6){
			$(".pass_tips").show();
			ev.preventDefault();
		}
		if($("#reg_pass_check").val() != $("#reg_pass").val()){
			$(".pass_tips_check").show();
			ev.preventDefault();
		}
 	});
 	//判断是用户登录还是管理员登录
 	$(".admini").click(function(){
 		if($(this).text() == '管理员登录'){
 			$(".log_title").text('管理员登录');
 			$(this).text('用户登录');
 		}else{
 			$(".log_title").text('用户登录');
 			$(this).text('管理员登录');
 		}
 	});
 	
 });